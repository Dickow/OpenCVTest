package moveableObjects;

public class Ball {
	
	private double x,y,radius;
	
	public Ball(double x, double y){
		this.x = x;
		this.y = y; 
		
	}

	
	public double getRadius() {
		return radius;
	}


	public void setRadius(double radius) {
		this.radius = radius;
	}


	public double getX() {
		return x;
	}

	public void setX(double x) {
		this.x = x;
	}

	public double getY() {
		return y;
	}

	public void setY(double y) {
		this.y = y;
	}
	
	

}
